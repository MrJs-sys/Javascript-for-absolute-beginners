//logical operator(&& || !)

let a = 5;
let b = 7;
let c = 9;

console.log(a<b && a<c) //both the conditions should be true

console.log(a<b || a>c) //either one of the must be true or both should be true

let d = a>c;
console.log(!d); //converts the true statement to false and the false to true



